# Arithmetic operators - perform simple calculations
x = 60
y = 7

print(x + y)  # Addition
print(x - y)  # Subtraction
print(x * y)  # Multiplication
print(x / y)  # Division
print(x % y)  # Modulus - returns the remainder

# Comparison Operator - Compare values
a = 34
b = 23
print(a < b)
print(a > b)
print(a <= b)
print(a >= b)
print(a == b)# equal to
print(a != b)# not equal to

# Assignment operators
num = 56
print(num)
z = 2
z += 4
print(z)

number = 45
number *= 2
print(number)

#Logical Operators
num1 = 23
num2 = 16
print(num1 > num2 and num1 == num2)
print(num1 > num2 or num1 == num2)
print(not(num1 > num2 and num1 == num2))